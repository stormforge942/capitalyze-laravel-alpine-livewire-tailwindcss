    <p class="border-b border-gray bg-gray-100 p-2"><b>Item 12 Small Businesses</b></p>
    <div class="border-b border-gray p-2">
        <p>The SEC is required by the Regulatory Flexibility Act to consider the effect of its regulations on small entities. In order to do this, we need to determine whether you meet the definition of "small business" or "small organization" under rule 0-7.</p>
        <p>Answer this Item 12 only if you are registered or registering with the SEC and you indicated in response to Item 5.F.(2)(c) that you have regulatory assets under management of less than $25 million. You are not required to answer this Item 12 if you are filing for initial registration as a state adviser, amending a current state registration, or switching from SEC to state registration.</p>
        <p>For purposes of this Item 12 only:</p>
        <ul>
            <li>Total Assets refers to the total assets of a firm, rather than the assets managed on behalf of clients. In determining your or another person's total assets, you may use the total assets shown on a current balance sheet (but use total assets reported on a consolidated balance sheet with subsidiaries included, if that amount is larger).</li>
            <li>Control means the power to direct or cause the direction of the management or policies of a person, whether through ownership of securities, by contract, or otherwise. Any person that directly or indirectly has the right to vote 25 percent or more of the voting securities, or is entitled to 25 percent or more of the profits, of another person is presumed to control the other person.</li>
        </ul>
